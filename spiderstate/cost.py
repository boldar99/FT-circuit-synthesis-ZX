def rank_mod2(matrix: np.ndarray) -> int:
    """Calculates the rank of a matrix over GF(2) using Gaussian elimination."""
    M = matrix.copy() % 2
    r, c = M.shape
    rank = 0

    for col in range(c):
        # 1. Find a pivot in the current column
        pivot_row = -1
        for row in range(rank, r):
            if M[row, col] == 1:
                pivot_row = row
                break

        # 2. If no pivot is found, move to the next column
        if pivot_row == -1:
            continue

        # 3. Swap the pivot row with the current rank row
        if pivot_row != rank:
            M[[rank, pivot_row]] = M[[pivot_row, rank]]

        # 4. Eliminate all other 1s in this column
        for row in range(r):
            if row != rank and M[row, col] == 1:
                M[row] = (M[row] + M[rank]) % 2

        rank += 1

        # Stop early if we've reached max possible rank
        if rank == min(r, c):
            break

    return rank


def check_span_equivalence(H_original: np.ndarray, H_new: np.ndarray) -> bool:
    """
    Checks if two parity-check matrices define the exact same code space over GF(2).
    """
    rank_orig = rank_mod2(H_original)
    rank_new = rank_mod2(H_new)

    # If the dimensions shrunk, the span is broken immediately
    if rank_orig != rank_new:
        print(f"Rank mismatch: Original={rank_orig}, New={rank_new}")
        return False

    # Stack the matrices vertically
    stacked_matrix = np.vstack((H_original, H_new))
    rank_stacked = rank_mod2(stacked_matrix)

    # If the stacked rank equals the original rank, the spans are identical
    is_equivalent = (rank_stacked == rank_orig)

    if not is_equivalent:
        print(f"Span mismatch: Stacked Rank={rank_stacked}, Expected={rank_orig}")

    return is_equivalent


